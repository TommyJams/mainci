function getTickets(){
	var dataString = 'eventId=9176&target=new';
	var response = $.ajax({
	   type: "POST",
	   data: dataString,
	   url: "/event/index/get-tickets",
	   success: function(response){
			$('#ticketEngine').html(response);
			viewHeight();			
	   }
	});	
}
function flipOption(classId) {
	$('#ticketMainView').html('');
	$('#progressBarOrg').show();
	var reqData = 'eventId=9176&categoryId='+classId+'&target=new';
	var response = $.ajax({
	   type: "POST",
	   url: "/event/index/get-ticket-type",
	   data: reqData,
	   success: function(response){
			$('#ticketMainView').html(response);	
			$('#ticketMainView').show();
		}
    }); 
}
var total			= 0;
function showCartValue(ticketId) {
	var qtySelected	= $('#'+ticketId+'_value_qty').val();
	var reqData 	= $('#frmTicketInfo').serialize();
	var response 	= $.ajax({
	   type: "POST",
	   dataType: "json",
	   url: "/event/index/generate-cart",
	   data: reqData,
	   success: function(response){
	   		if(response.carts.codeError == true) {
				$('#messageDivModal').html('Your entered discount code is not valid');
				$("#messageModal").modal({
					close:true
				});	
			}
			$('.finalPrice').html('');
			$('#mainPriceTotal').hide();
			$('#mainPriceTotalBlank').show();
			$.each(response.carts.cart, function(entryIndex, entry) {
				if(entry.ticketTypeId) {
					if(entry.rowPrice == 0.00) {
						$('#finalPrice_'+entry.ticketTypeId).html(0);	
						$('#'+entry.ticketTypeId+'_currency').show();
					}else {
						$('#finalPrice_'+entry.ticketTypeId).html(entry.rowPrice);
						$('#'+entry.ticketTypeId+'_currency').show();
					}
				}
			});
			if(response.carts.conversion != '0.00') {
				$('#converted').html('Note: 1 USD = '+response.carts.conversion+' INR');	
			}
			if(response.carts.total == '0.00') {
				$('.finalPriceMainTotal').html(response.carts.actualCost);
				$('.finalPriceMainProcessing').html(response.carts.servieFeeTotal);
				$('.finalPriceDiscount').html(response.carts.totalDiscount);
				$('.finalGrandTotal').html(response.carts.grandTotal);	
				$('.finalServiceFee').html(response.carts.serviceTax);
			}else {
				total		= response.carts.total;
				$('.finalPriceMainTotal').html(response.carts.actualCost);
				$('.finalPriceMainProcessing').html(response.carts.servieFeeTotal);
				$('.finalPriceDiscount').html(response.carts.totalDiscount);
				$('.finalGrandTotal').html(response.carts.grandTotal);
				$('.finalServiceFee').html(response.carts.serviceTax);
			}
		}
   });
}
</script>





